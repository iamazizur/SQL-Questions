  MAX_SALARY AS
  (
	SELECT MAX(SALARY) AS MAX_SALARY FROM employee
  ),
  MIN_SALARY AS
  (
	SELECT MIN(SALARY) AS MIN_SALARY FROM employee
  )
  SELECT * FROM MAX_SALARY,MIN_SALARY;
